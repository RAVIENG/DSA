public class _switch{
    public static void main(String args[]){
        int number= 2;
        switch(number){
            case 1:System.out.println("your name is ravi");
            break;
            case 2:System.out.println("where are you");
            break;
            case 3:System.out.println("my name is ");
            break;
            default:System.out.println("who are you");
        }
    }
}
